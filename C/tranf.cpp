#include <iostream>
#include <iomanip> // 用于控制打印精度

using namespace std;

int main() {
    cout << "====== C++ 隐式类型转换：物理车祸与奇迹沙盘 ======\n\n";

    // ---------------------------------------------------------
    // 规则 1 & 2：布尔值的生与死
    // ---------------------------------------------------------
    cout << "【1. 布尔转换】\n";
    bool b_alive = -999; // 非0即为true(1)
    bool b_dead = 0;     // 0就是false(0)
    cout << "  -999 变成 bool: " << b_alive << " (代表 true)\n";
    cout << "  0    变成 bool: " << b_dead << " (代表 false)\n";

    int truth = true;
    cout << "  true 变成 int : " << truth << " (绝对纯粹的 1)\n\n";

    // ---------------------------------------------------------
    // 规则 3 & 4：浮点铡刀与尾数溢出
    // ---------------------------------------------------------
    cout << "【2. 整数与浮点数阶级跨越】\n";
    int chopped = 3.9999; // 铡刀落下！无情砍掉小数
    cout << "  3.9999 变成 int: " << chopped << " (绝不四舍五入！)\n";

    // 模拟精度丢失灾难
    int huge_int = 2147483647; // int 的极限
    float lossy_float = huge_int;
    // float 精度只有7位，强行装21亿会导致低位数据畸变
    cout << "  2147483647 变成 float: " << fixed << setprecision(0) << lossy_float << " (尾数已经畸变损失了！)\n\n";

    // ---------------------------------------------------------
    // 规则 5：无符号的里程表倒转 (取模机制)
    // ---------------------------------------------------------
    cout << "【3. 无符号类型的里程表倒转】\n";
    // unsigned char 容量是 1 字节，范围 0~255，总数 256
    unsigned char u_c1 = -1;  
    unsigned char u_c2 = 257; 
    
    // 注意：打印 char 类型会被当成字符，所以我们要把它强转成整数 (int) 来看真实的物理数字
    cout << "  -1 强塞进 unsigned char: " << (int)u_c1 << " (-1 取模 256)\n";
    cout << "  257 强塞进 unsigned char: " << (int)u_c2 << " (257 取模 256)\n\n";

    // ---------------------------------------------------------
    // 规则 6：带符号类型的未定义行为 (Undefined Behavior)
    // ---------------------------------------------------------
    cout << "【4. 带符号溢出：未定义行为 (UB)】\n";
    signed char s_c = 127; // signed char 最大只能装 127
    cout << "  目前 s_c 是最大极限: " << (int)s_c << "\n";
    
    s_c = s_c + 1; // 物理越界！触发 Undefined Behavior！
    // 编译器撒手不管，在主流 x86 CPU 上通常会直接跳到极小值
    cout << "  加 1 后发生物理溢出: " << (int)s_c << " (系统分配的垃圾/溢出数值！)\n";

    cout << "\n==================================================\n";
    return 0;
}